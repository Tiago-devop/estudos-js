// includes: Retorna verdadeiro se a String contém a String passado por parâmetro
console.log("JavaScript".includes("Java"))

// startsWith: Retorna verdadeiro se a String inicia com a String passada por parâmetro
console.log("Ruby".startsWith("R"))

// endsWith: Retorna verdadeiro se a String termina com a String passada por parâmetro
console.log("Erlang".endsWith("lang"))