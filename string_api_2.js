// charAt: Retorna o caractere na posição passada por parâmetro
console.log("JavaScript".charAt(1))

// charCodeAt: Retorna o código com base na posição passada por parâmetro
console.log("JavaScript".charCodeAt(1))

// fromCharCode: Retorna um caracterer com base no código passado por parâmetro
console.log(String.fromCharCode(97))