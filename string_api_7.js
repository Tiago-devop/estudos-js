// concat: Retorna uma nova String resultante da concatenação da que está invocando a
// função e da outra, passada por parâmetro
console.log("Java".concat("Script"))
console.log("Data".concat("Flex"))

// padStart: Completa a String com caracteres no início
console.log("Script".padStart(10, "Java"))

// padEnd: Completa a String com caracteres no fim
console.log("C".padEnd(3, "+"))

// repeat: Repete um caractere
console.log("C".concat("+".repeat(2)))

// trim: Elimina espaços em branco no início e no fim
console.log(" Self ".trim())

// trimLeft: Elimina espaços em branco no início
console.log(" Scheme ".trimLeft())

// trimRight: Elimina espaços em branco no fim
console.log(" Perl ".trimRight())